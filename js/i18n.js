export const STRINGS = { en, it, de, at, fr, es, pt, hu, hr };

export const HTML_LANG = {
  en: 'en', it: 'it', de: 'de', at: 'de-AT',
  fr: 'fr', es: 'es', pt: 'pt', hu: 'hu', hr: 'hr',
};

export function detectLang() {
  const stored = localStorage.getItem('ndp-lang');
  if (stored && STRINGS[stored]) return stored;
  const nav = (navigator.language || 'en').toLowerCase();
  if (nav.startsWith('it')) return 'it';
  if (nav === 'de-at' || nav.startsWith('de-at')) return 'at';
  if (nav.startsWith('de')) return 'de';
  if (nav.startsWith('fr')) return 'fr';
  if (nav.startsWith('es')) return 'es';
  if (nav.startsWith('pt')) return 'pt';
  if (nav.startsWith('hu')) return 'hu';
  if (nav.startsWith('hr')) return 'hr';
  return 'en';
}
